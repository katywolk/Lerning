const collection = {};

for (const item of array) {
    let type = typeof item;

    if (Array.isArray(item)) {
        type = 'array';
    }

    else if (item === null) {
        type = 'null';
    }
    if (collection[type]) {
        collection[type]++;
    } else {
        collection[type] = 1;
    }
}
console.dir(collection);

